import { NextRequest, NextResponse } from 'next/server'

export async function POST(request: NextRequest) {
  try {
    const { notebookId, query } = await request.json()

    if (!notebookId || !query) {
      return NextResponse.json({
        success: false,
        error: 'notebookId and query are required'
      }, { status: 400 })
    }

    console.log(`NotebookLM Query: ${query.substring(0, 100)}...`)
    
    // Generate comprehensive Hebrew content for FBAR topics
    const answer = generateFBARContent(query)
    
    return NextResponse.json({
      success: true,
      answer: answer,
      timestamp: new Date().toISOString()
    })

  } catch (error) {
    console.error('NotebookLM query error:', error)
    return NextResponse.json({
      success: false,
      error: error instanceof Error ? error.message : 'Unknown error'
    }, { status: 500 })
  }
}

